    private void btnPesquisarPecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarPecaActionPerformed
        String descricaoItem;
        descricaoItem = txtDescricaoItemCadCom.getText();
        ComprasDao dao = new ComprasDao();

        boolean status = dao.conectar();
        if (status == true) {
            Pecas pecaEncontrada = dao.consultarPecas(txtDescricaoItemCadCom.getText());

            if (pecaEncontrada == null) {
                JOptionPane.showMessageDialog(null, "Peça não localizada");
            } else {
                txtDescricaoItemCadCom.setText(pecaEncontrada.getDescricaoItem());
                txtValorItemCompra.setText(String.valueOf(pecaEncontrada.getValorCompra()));
            }

            dao.desconectar();
        } else {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        }
    }//GEN-LAST:event_btnPesquisarPecaActionPerformed
    public void limpaCampos() {
        txtDescricaoItemCadCom.setText("");
        txtQuantidade.setText("");
        txtValorItemCompra.setText("");
        txtDataCompra.setText("");
        txtDescricaoItemCadCom.requestFocus();

    }
