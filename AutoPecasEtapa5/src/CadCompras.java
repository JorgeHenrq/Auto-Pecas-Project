    private void btnCadastroCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroCompraActionPerformed
        Compras comp = new Compras();
        ComprasDao dao = new ComprasDao();
        boolean status;
        int resposta;

        comp.setDescricaoItemCompras(txtDescricaoItemCadCom.getText());
        comp.setQuantidadeCompra(Integer.parseInt(txtQuantidade.getText()));
        comp.setValorCompra(Double.parseDouble(txtValorItemCompra.getText()));
        comp.setDataCompra(txtDataCompra.getText());

        dao = new ComprasDao();

        status = dao.conectar();
        if (status == false) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        } else {
            resposta = dao.salvar(comp);
            if (resposta == 1) {
                JOptionPane.showMessageDialog(null, "Dados incluidos com sucesso");
                //limpar os campos
                txtDescricaoItemCadCom.setText("");
                txtQuantidade.setText("");
                txtValorItemCompra.setText("");
                txtDataCompra.setText("");

                //posicionar o cursor para um próximo
                txtDescricaoItemCadCom.requestFocus();
            } else if (resposta == 1062) {
                JOptionPane.showMessageDialog(null, "Filme já foi cadastrado");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar inserir dados");
            }
            limpaCampos();
            dao.desconectar();
        }
    }//GEN-LAST:event_btnCadastroCompraActionPerformed

