package model;


public class Compras {
    String descricaoItemCompras;
    int quantidadeCompra;
    double valorCompra;
    String dataCompra;

    public String getDescricaoItemCompras() {
        return descricaoItemCompras;
    }

    public void setDescricaoItemCompras(String descricaoItemCompras) {
        this.descricaoItemCompras = descricaoItemCompras;
    }

   

    public int getQuantidadeCompra() {
        return quantidadeCompra;
    }

    public void setQuantidadeCompra(int quantidadeCompra) {
        this.quantidadeCompra = quantidadeCompra;
    }

    public double getValorCompra() {
        return valorCompra;
    }

    public void setValorCompra(double valorCompra) {
        this.valorCompra = valorCompra;
    }

    public String getDataCompra() {
        return dataCompra;
    }

    public void setDataCompra(String dataCompra) {
        this.dataCompra = dataCompra;
    }
    
    
}
